package pratice;

public class EncapsulationRect {
	private  int i;
	private int w;
	private int a;
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	
	
	
	

}
